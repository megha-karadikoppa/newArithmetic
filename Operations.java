public class Operations{
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int sum = a + b;
        int sub = a - b;
        int mul = a * b;
        int div = a / b;
        System.out.println("sum is=" + sum);
        System.out.println("sub is=" + sub);
        System.out.println("product is=" + mul);
        System.out.println("quotient is=" + div);
    }
}